#include <stdio.h> // Pre-processor Directive printf()

int main() {
    int i;
	printf("100 to 50 in Descending Order.\n" );
	for( i=100; i >=50;i--){  //descending order instructions. 
		printf("%d\n", i);
	}
	return 0;

}
